def average_of_numbers(numbers):
    total_numbers = sum(numbers)
    count_of_numbers = len(numbers)
    average = total_numbers / count_of_numbers
    print(f"–°–µ—Ä–µ–¥–Ω—î –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–Ω–µ: {average}")

