#Порахувати кількість унікальних символів в строці.
# Якщо їх більше 10 - вивести в консоль True, інакше - False.
# Строку отримати за допомогою функції input()


# Вводимо рядок
text = input("Введи рядок: ")

# Створюємо множину унікальних символів
unique_chars = set(text)

# Друкуємо унікальні символи
print("Унікальні символи:", unique_chars)

# Перевіряємо умову і виводимо True або False
print("Більше 3 унікальних символів?", len(unique_chars) > 3)