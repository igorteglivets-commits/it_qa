#Є list з даними lst1 = ['1', '2', 3, True, 'False', 5, '6', 7, 8, 'Python', 9, 0, 'Lorem Ipsum'].
# Напишіть код, який сформує новий list (наприклад lst2),
# який містить лише змінні типу стрінг, які присутні в lst1.
# Данні в лісті можуть бути будь якими



# Початковий список
lst1 = ['1', '2', 3, True, 'False', 5, '6', 7, 8, 'Python', 9, 0, 'Lorem Ipsum']

# Створюємо порожній список для рядків
lst2 = []

# Перебираємо всі елементи lst1
for item in lst1:
    if isinstance(item, str):  # перевіряємо, чи елемент рядок
        lst2.append(item)       # додаємо в новий список

# Виводимо результат
print(lst2)
