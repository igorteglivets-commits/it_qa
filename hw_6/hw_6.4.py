#Є лист з числами, порахуйте суму усіх ПАРНИХ чисел в цьому листі



# Приклад списку з числами
numbers = [1, 2, 3, 4, 5,]

# Створюємо змінну для суми парних чисел
even_sum = 0

# Перебираємо всі числа у списку
for num in numbers:
    if num % 2 == 0:  # перевіряємо, чи число парне
        even_sum += num  # додаємо його до суми

# Виводимо результат
print("Сума всіх парних чисел:", even_sum)
